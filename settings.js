        if (((p1 = thisGhost.position), (p2 = nextPosition), p1.x === p2.x && p1.y === p2.y)) {
            const nextTargetIdx = points.indexOf(nextPosition) + 1;
            nextPosition = nextTargetIdx >= points.length ? points[0] : points[nextTargetIdx];
        }
